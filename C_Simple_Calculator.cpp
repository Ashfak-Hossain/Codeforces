#include <bits/stdc++.h>

using namespace std;

int main()
{
    long long n, m;
    cin >> n >> m;

    cout << n << " + " << m << " = " << n + m << endl;
    cout << n << " * " << m << " = " << n * m << endl;
    cout << n << " - " << m << " = " << n - m << endl;

    return 0;
}